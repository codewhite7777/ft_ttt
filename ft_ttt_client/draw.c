void print_board(char board[][3])
{
    system("clear");
    for (int i = 0; i < 3; ++i)
    {
        
        printf("\x1b[0m -------------------- -------------------- --------------------\n");
        for (int y = 0 ; y < 12; ++y)
        {
            printf("\x1b[0m|");
            for (int x = 0 ; x < 3 ; ++x)
            {
            if (board[i][x] == 'O')
                printf("%s", t1[y]);
            else if (board[i][x] == 'X')
                printf("%s", t2[y]);
            else
                printf("%s", state[3 * i + x][y]);
            printf("\x1b[0m|");
            }
            printf("\n");
        }
    }
    printf("\x1b[0m -------------------- -------------------- --------------------\n");
}

void    print_ending(t_game *g_info, enum e_state state) {
    system("clear");
    if ((state == O_WIN && g_info->stone == STONE_O) \
    || (state == X_WIN && g_info->stone == STONE_X))
    {
        for (int i = 0; i < 7; ++i)
            printf("%s", last[0][i]);
    }
    else if (state == DRAW)
    {
        for (int i = 0; i < 7; ++i)
            printf("%s", last[2][i]);
    }
    else
    {
        for (int i = 0; i < 7; ++i)
            printf("%s", last[1][i]);
    }
}
